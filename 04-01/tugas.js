// Segitiga A
const alasSgtgA = 10;
const tinggiSgtgA = 5;
const luasSgtgA = 0.5 * alasSgtgA * tinggiSgtgA

// Segitiga B
const alasSgtgB = 10;
const tinggiSgtgB = 5;
const luasSgtgB = 0.5 * alasSgtgB * tinggiSgtgB


if (luasSgtgA == luasSgtgB) {
    document.writeln('Luas Segitiga A <strong>sama</strong> dengan Segitiga B </br>')
}
else {
    document.writeln('Luas Segitiga A <strong>tidak sama</strong> dengan Segitiga B </br>')
}

